def is_Even(number):
    if number == 1:
        return False
    elif number == 2:
        return True
    elif number == 3:
        return False
    elif number == 4:
        return True
    elif number == 5:
        return False
    elif number == 6:
        return True
    elif number == 7:
        return False
    elif number == 8:
        return True
    elif number == 9:
        return False
    elif number == 10:
        return True
